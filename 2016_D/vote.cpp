double vote(int N, int M)
{
	return (double)(N - M) / (N + M);
}

int main()
{
	int T;
	cin >> T;

	for (int t = 1; t <= T; ++t)
	{
		int N, M;
		cin >> N >> M;

		cout << "Case #" << t << ": ";
		cout << fixed << setprecision(9) << vote(N, M) << "\n";
	}

	return 0;
}

